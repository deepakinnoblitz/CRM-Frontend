import type { ReactNode } from 'react';

import { Navigate } from 'react-router-dom';

import Box from '@mui/material/Box';
import CircularProgress from '@mui/material/CircularProgress';

import { hasValidRole } from 'src/layouts/nav-config-dashboard';

import { useAuth } from './auth-context';

type AuthGuardProps = {
  children: ReactNode;
};

export function AuthGuard({ children }: AuthGuardProps) {
  const { user, loading } = useAuth();

  // Show a full-page spinner while checking session — no more blank white screen
  if (loading) {
    return (
      <Box
        sx={{
          display: 'flex',
          height: '100vh',
          alignItems: 'center',
          justifyContent: 'center',
          bgcolor: 'background.default',
        }}
      >
        <CircularProgress />
      </Box>
    );
  }

  // Session expired or not logged in → go to sign-in
  if (!user) {
    return <Navigate to="/sign-in" replace />;
  }

  // Check if user has valid roles
  const userRoles = user.roles || [];
  const hasAccess = hasValidRole(userRoles);

  if (!hasAccess) {
    return <Navigate to="/access-denied" replace />;
  }

  return <>{children}</>;
}
